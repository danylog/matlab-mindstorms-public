            strokes{1} = [0, 0; 1, 1];
            strokes{2} = [0, 1; 1, 0];
            
        case 'Y'
            strokes{1} = [0, 1; 0.5, 0.5; 1, 1];
            strokes{2} = [0.5, 0.5; 0.5, 0];
            
