        case '3'
            strokes{1} = [0, 0.85; 0.3, 1; 0.7, 1; 1, 0.85; 1, 0.6; 0.7, 0.5; 0.4, 0.5];
            strokes{2} = [0.7, 0.5; 1, 0.4; 1, 0.15; 0.7, 0; 0.3, 0; 0, 0.15];
            
