        case 'U'
            strokes{1} = [0, 1; 0, 0.2; 0.3, 0; 0.7, 0; 1, 0.2; 1, 1];
            
        case 'V'
            strokes{1} = [0, 1; 0.5, 0; 1, 1];
            
