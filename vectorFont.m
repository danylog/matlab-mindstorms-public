        case ':'
            strokes{1} = [0.4, 0.2; 0.4, 0.3; 0.6, 0.3; 0.6, 0.2; 0.4, 0.2];
            strokes{2} = [0.4, 0.6; 0.4, 0.7; 0.6, 0.7; 0.6, 0.6; 0.4, 0.6];
            
        case ';'
            strokes{1} = [0.5, 0.15; 0.5, 0; 0.3, -0.15];
            strokes{2} = [0.4, 0.5; 0.4, 0.6; 0.6, 0.6; 0.6, 0.5; 0.4, 0.5];
            
        case '/'
            strokes{1} = [0, 0; 1, 1];
            
        case '\'
            strokes{1} = [0, 1; 1, 0];
            
        case '('
            strokes{1} = [0.7, 0; 0.3, 0.2; 0.3, 0.8; 0.7, 1];
            
        case ')'
            strokes{1} = [0.3, 0; 0.7, 0.2; 0.7, 0.8; 0.3, 1];
            
        case '['
            strokes{1} = [0.7, 0; 0.3, 0; 0.3, 1; 0.7, 1];
            
        case ']'
            strokes{1} = [0.3, 0; 0.7, 0; 0.7, 1; 0.3, 1];
            
        case '+'
