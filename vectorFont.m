            strokes{1} = [0.7, 0; 0.7, 1; 0, 0.3; 1, 0.3];
            
        case '5'
            strokes{1} = [1, 1; 0, 1; 0, 0.5; 0.7, 0.5; 1, 0.35; 1, 0.15; 0.7, 0; 0.3, 0; 0, 0.15];
            
